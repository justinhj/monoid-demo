import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        byte b;
        while(sc.hasNextByte()) {
            b = sc.nextByte();
            System.out.print(b);
        }
        sc.close();
        
        
    }
}